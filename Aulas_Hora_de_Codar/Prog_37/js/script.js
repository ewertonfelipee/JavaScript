// keydown
document.addEventListener("keydown", function(e) {

    // console.log(e.key);
    if(e.key == "Enter")
        console.log("Enter key pressed");
});

// keydown
document.addEventListener("keyup", function(e) {

    // console.log(e.key);
    if(e.key == "Enter")
        console.log("dropped off the key");
});